package com.myweb.domain;

import java.io.Serializable;

public class JiHua implements Serializable {

    private String line;
    private String name;
    private String type;
    private String bumen;
    private String sheng;
    private String shi;
    private String yue;
    private String liang;
    private String mindan;
    private String xiangshu;
    private String youxianji;
    public String getLine() {
        return line;
    }

    public void setLine(String line) {
        this.line = line;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getType() {
        return type;
    }

    public void setType(String type) {
        this.type = type;
    }

    public String getBumen() {
        return bumen;
    }

    public void setBumen(String bumen) {
        this.bumen = bumen;
    }

    public String getSheng() {
        return sheng;
    }

    public void setSheng(String sheng) {
        this.sheng = sheng;
    }

    public String getShi() {
        return shi;
    }

    public void setShi(String shi) {
        this.shi = shi;
    }

    public String getYue() {
        return yue;
    }

    public void setYue(String yue) {
        this.yue = yue;
    }

    public String getLiang() {
        return liang;
    }

    public void setLiang(String liang) {
        this.liang = liang;
    }

    public String getMindan() {
        return mindan;
    }

    public void setMindan(String mindan) {
        this.mindan = mindan;
    }

    public String getXiangshu() {
        return xiangshu;
    }

    public void setXiangshu(String xiangshu) {
        this.xiangshu = xiangshu;
    }

    public String getYouxianji() {
        return youxianji;
    }

    public void setYouxianji(String youxianji) {
        this.youxianji = youxianji;
    }
}